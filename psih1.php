                                <p style="font-size: 25px;"><b>Что делать, если ребёнок с трудом привыкает к школе?</b></p>

                                <p><i>
                                	Проявляйте максимум терпения и сообразительности.<br>
                                	В отношениях с ребёнком придерживайтесь «позитивной модели», подчёркивайте его успехи, даже самые незначительные, непременно поощряйте, хвалите его в тех случаях, когда он этого заслуживает( даже если успех невелик и не совсем удовлетворяет учителя и вас).<br>
                                	Не фиксируйте внимание ребёнка на оценках, которые он получает. Оценивайте его внимание учиться, старание.<br>
                                	Оценивая успехи, не сравнивайте своего ребёнка с другими детьми, не подчёркивайте, что кто–то делает это лучше, особенно если видите, что ребёнок прилагает много усилий. Помните: каждый ребёнок уникален!<br>
                                	Серьёзно, по-взрослому объясняйте ребёнку правила поведения в школе. Можно посетовать на огорчение, которое он доставляет вам плохим поведением, но обязательное правило – делайте это наедине, не ставьте ребёнка в неловкое положение ни перед другими взрослыми, ни перед сверстниками (особого внимания в этом отношении требуют дети, которые не посещают детский сад, а воспитываются дома).<br>
                                	Ребёнок должен знать, что у вас он всегда найдёт поддержку, сочувствие, должен быть уверенным, что вы поможете ему найти выход из любой трудной ситуации.<br>
                                	Если вы чувствуете, что не справляетесь с возникшими проблемами, обратитесь к психологу.</i>
                                </p>

