								<p style="font-size: 25px;"><b>Рекомендации для родителей, у которых ребёнок стал первоклассником</b></p>
								<p>• Ваш ребёнок пошёл в школу. Не считайте, что в школу пошли вы. У вас много дел и без этого.<br>
                                • Не старайтесь увидеть в ребёнке десятиклассника. Будьте готовы, что учеником он станет спустя некоторое время. Парта, ранец, тетради ещё не делают из него ученика.<br>
                                • Уважайте новую жизнь ребёнка. Теперь уже не вы решаете, с кем он сидит за одной партой, какими должны быть его взаимоотношения с учительницей и одноклассниками. Всё это теперь его собственные уроки жизни, которые, поверьте, важнее школьного расписания.<br>
                                • Меньше отвечайте за его пребывание в школе и больше – за пребывание дома. Дом теперь приобретает для ребёнка гораздо большее значение, чем раньше.<br>
                                • Старайтесь не демонстрировать возможностей ребёнка перед сверстниками. Важнее, чтобы он почувствовал себя равным другим, хотя бы в первом классе.<br>
                                • Попробуйте запомнить имена учителей, одноклассников, их родителей. Иногда это может оказаться важным в самых неожиданных обстоятельствах.<br>
                                • Старайтесь не заходить в магазины и не делайте побочных дел по дороге в школу. Беседуйте с ребёнком, спрашивайте об уроках, которые сегодня предстоят. То есть проявляйте уважение к школе как самому важному делу вашего ребёнка.<br>
                                • На первых порах просите ребёнка «подсказать» вам, как дойти до дома. Советуйтесь: а что, если мы пройдём по другой улице? Ребёнку важно, проведя несколько часов в школе, почувствовать пространство. Просто побыть в движении.<br>
                                • Дойдите с ребёнком до одной или двух соседних школ. Он должен ощущать, что его школа – не единственная.<br>
                                • Помогайте ребёнку поддерживать связь с его дошкольной жизнью. Зайдите вместе с ним в детский сад, чтобы поздороваться с воспитателями, рассказать о школе. Дайте покачаться на качелях. Загляните в гости к кому-то из соседских детей, с кем ваш ребёнок дружил до школы.<br>
                                • И ещё! Человек, который пошёл в школу, преисполнится достоинства, если у него появится собственный будильник, который он с вечера сам поставит на определённое время.<br>
                                • И ещё очень важно – уважение к миру ребёнка. Не заглядывайте в ранец без его ведома. Он должен быть уверен, что отныне без его разрешения никто не будет трогать вещи, принадлежащие ему.<br>
                                Хорошо бы завести тетрадь для ежедневных записей или рисунков. В этой тетради он сможет описывать каждый свой день.
								</p>

