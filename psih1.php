							<h2 class="title">Советы психолога родителям первоклассников</h2><br>
                            <div class="text">
								<img src="images/psih1.jpg" style="width:350px ; height:220px; float: left;" />
								<p>Школа… Как много ожиданий, надежд, волнений связывают дети, родители, учителя с этим словом.
                                 Поступление в школу – это начало нового этапа в жизни ребёнка, вхождение его в мир знаний. Новых прав и обязанностей, сложных и разнообразных взаимоотношений со взрослыми и сверстниками.<br>У вас Событие – ваш ребёнок впервые переступил порог школы. Как он будет успевать в школе, понравится ли ему быть учеником, как сложатся взаимоотношения с учителем, одноклассниками? Эти тревоги одолевают всех родителей, даже если в школу идёт уже второй, третий или пятый ребёнок.
                                 Это естественно, поскольку каждый маленький человек – неповторим, у него свой внутренний мир, свои интересы, свои способности и возможности. И главная задача родителей совместно с педагогами – так организовать обучение, чтобы ребёнок с удовольствием посещал школу, познавал окружающий мир и, конечно, хорошо учился.</p>

                                <center><img src="images/psih11.png" style="width:300px ; height:300px;" /></center>

								<p>Как должны вести себя взрослые, чтобы добиться этого? Необходима «кровная» заинтересованность в успехах, в школьных делах маленького ученика. Он должен чувствовать, что родителям, дедушкам и бабушкам очень важно и интересно знать, что происходило в школе, что нового ( по каждому предмету в отдельности) узнал он сегодня. Желательно поддерживать интерес к учёбе, перенося новые знания ребёнка на повседневную жизнь (использовать навык счёта, чтобы посчитать, сколько птиц село на ветку или сколько красных машин стоит у дома, навык чтения – чтобы прочитать вывеску или название новой книжки, купленной мамой).<br>И, конечно, необходимо поощрять каждое маленькое и большое достижение вашего отпрыска. Дело в том, что особенно в возрасте 6 -10 лет дети ориентированы на реакцию взрослых. Они очень чутко отзываются на похвалу или порицание родителей, учителя, стараются привлечь к себе внимание, почувствовать себя нужными и любимыми ( хорошими). Поэтому для пап и мам, бабушек и дедушек это реальный рычаг для поддержания и повышения интереса к школе и обучению.<br>О вреде длительного просмотра телевизионных передач, игры в компьютер знают все от мала до велика. Можно сделать эту неотъемлемую и любимую практически всеми детьми часть нашей жизни способом поощрения за успехи и достижения. Тем более что обязанности школьника требуют много сил, времени и здоровья.<br>Чтобы кроме внешних атрибутов школьной жизни ( портфель, тетради, учебники и т. п.) появилось внутреннее ощущение перехода в новое качество « ученик», необходимо отношение взрослых к поступлению в школу как к ответственному, серьёзному шагу ребёнка ( «ты теперь ученик, большой мальчик, у тебя новы, серьёзные обязанности»). Конечно, ваше дитя будет продолжать играть и в куклы и в машинки, но нужно давать установку на « взросление». А это не только новые обязанности, но и новые возможности, более сложные поручения и определённая самостоятельность. Контроль необходим ( степень его проявления в компетентности каждого родителя), но всё же постарайтесь дать возможность вашему первокласснику «подрасти» в своём мироощущении, почувствовать себя старше.<br>У каждой личности должно быть своё пространство. Если у ребёнка нет своей комнаты, нужно организовать рабочее место: письменный стол, где он будет заниматься своим серьёзным делом – учиться. Это хорошо и сточки зрения соблюдения правил гигиены – правильная посадка, позволяющая сохранить осанку. Необходимое освещение.</p>

								<p>Отдельного внимания заслуживает режим дня. К сожалению, многие дети (по статистике, порядка 40 – 50 %) обладают синдромом минимальной мозговой дисфункции (ММД) или, как следствие, вегетососудистой дистонией.<br>Эти лёгкие неврологические заболевания могут скомпенсироваться к 10- 12 годам при условии соблюдения общей гигиены жизни (нормальная продолжительность сна – 9 – 10 часов, прогулки — 2 — 3 часа, подвижные игры и занятия спортом, посещение бассейна, полноценное питание). Именно для этой категории детей важно правильно распределить время учёбы и отдыха, поскольку причинами проблем с учёбой, поведением являются повышенная утомляемость, невозможность длительной концентрации внимания.<br>Пожалуйста, дорогие родители. не переусердствуйте в выполнении домашних заданий. Дети в возрасте 6 -7 лет должны заниматься не более получаса, далее нужно делать перерыв не менее 15 минут. Количество не всегда переходит в качество! Кроме того, длительным написанием палочек и крючочков можно надолго отбить охоту к учёбе.<br>Ещё всем родителям нелишне знать, что все мы грешны высокими амбициями и ожиданиями по отношению к своим чадам. И бывает очень сложно детям дотянуться до той планки, которую мы им ставим. Бесспорно. Проще стучать кулаком по столу и требовать: «Учись хорошо, слушай учительницу!» Гораздо сложнее помогать ребёнку, ежедневно разбираясь в его проблемах, подбадривать в случае неудачи и хвалить за успехи.<br>Помните: ребёнок – это чистый лист, который нам предстоит заполнить. И от того, как мы будем это делать, зависит образ будущей личности.</p>
                                
