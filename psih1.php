								<p><b>Какие знания должны быть сформированы у будущего первоклассника?
								В области развития речи и готовности к овладению грамотой будущего первоклассника необходимо:</b></p>

								<p>• уметь чётко произносить все звуки речи;<br>
								• уметь интонационно выделять звук в словах;<br>
								• уметь выделять заданный звук в потоке речи;<br>
								• уметь определять место звука в слове ( в начале, в середине, в конце);<br>
								• уметь произносить слова по слогам;<br>
								• уметь произносить предложения из 3 – 5 слов;<br>
								• уметь называть в предложении только 2-е слово, только 3-е слово и т.д.;<br>
								• уметь использовать обобщающие понятия ( медведь, лиса, волк – это животные);<br>
								• уметь составлять рассказ по картинке ( например, «В зоопарке», « На детской площадке», «Отдых на море», «За грибами» и т. д.);<br>
								• уметь составлять несколько предложений о предмете;<br>
								• различать жанры художественной литературы ( сказка, рассказ, стихотворение, басня);<br>
								• уметь наизусть читать любимые стихотворения;<br>
							    • знать автора прочитанного стихотворения;<br>
						        • уметь последовательно передавать содержание сказки;</p>

								<img src="images/psih14.jpg" style=" width:350px ; height:250px; float: left;" />

							    <p><b>К началу обучения в школе у ребёнка должны быть развиты элементарные математические представления:</b></p>
							    <p>
							    	• знать цифры от 0 до 9;<br>
							    	• уметь считать до 10 и обратно, от 6 до 10, от 7 до 2 и т. д.;<br>
							    	• уметь называть предыдущее и последующее число относительно любого числа в пределах первого десятка;<br>
							    	• знать знаки +, -, =,<br>
							    	• уметь сравнивать числа первого десятка ( например. 7 8,, 5 4, 6=6);<br>
							    	• уметь соотносить цифру и число предметов;<br>
							    	• уметь сравнивать 2 группы предметов;<br>
							    	• уметь составлять и решать задачи в одно действие на сложение и вычитание;<br>
							    	• знать названия фигур: треугольник, квадрат, круг;<br>
							    	• уметь сравнивать предметы по цвету, размеру, форме;<br>
							    	• уметь оперировать понятиями: «налево», «направо», «вверх», «вниз», «раньше», «позже», «перед», «за», «между»;<br>
							    	• уметь группировать по определённому признаку предложенные предметы.
							    </p>

							    <p><b>В области представлений об окружающем мире будущему первокласснику необходимо:</b></p>
							    <p>
							    	• уметь различать по внешнему виду растения, распространённые в нашей местности ( например, ель, берёза, дуб, подсолнух, ромашка) и назвать их отличительные признаки;<br>
							    	• уметь различать по внешнему виду птиц ( например, дятел, ворона, воробей…);<br>
							    	• иметь представления о сезонных признаках природы ( например, осень – жёлтые и красные листья на деревьях, увядающая трава, сбор урожая…)<br>
							    	• знать названия 1 -3 комнатных растений;<br>
							    	• знать названия 12 месяцев года;<br>
							    	• знать названия всех дней недели.<br>							    	
							    </p>

							    <p><b>Кроме того, ребёнок, поступающий в 1-й класс, должен знать:</b></p>
							    <p>
							    	• в какой стране он живёт, в каком городе, на какой улице, в каком доме;<br>
							    	• полные имена членов своей семьи, иметь общие понятия о различных видах их деятельности;<br>
							    	• знать правила поведения в общественных местах и на улице.<br>							    	
							    </p>

